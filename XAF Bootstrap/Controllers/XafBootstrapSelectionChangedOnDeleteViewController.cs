    // For more typical usage scenarios, be sure to check out https://documentation.devexpress.com/eXpressAppFramework/clsDevExpressExpressAppViewControllertopic.aspx.
    public partial class XafBootstrapSelectionChangedOnDeleteViewController : ViewController
    {
        public XafBootstrapSelectionChangedOnDeleteViewController()
        {
            InitializeComponent();
            // Target required Views (via the TargetXXX properties) and create their Actions.
        }
        protected override void OnActivated()
        {
            base.OnActivated();
            Frame.GetController<DeleteObjectsViewController>().DeleteAction.ExecuteCompleted += DeleteAction_ExecuteCompleted;
        }

        void DeleteAction_ExecuteCompleted(object sender, ActionBaseEventArgs e)
        {
            ListEditor editor = (View as ListView).Editor;
            XafBootstrapTableEditor tableEditor = editor as XafBootstrapTableEditor;
            if (tableEditor != null)
                tableEditor.Refresh();
        }
        protected override void OnViewControlsCreated()
        {
            base.OnViewControlsCreated();
            // Access and customize the target View control.
        }
        protected override void OnDeactivated()
        {
            // Unsubscribe from previously subscribed events and release other references and resources.
            base.OnDeactivated();
            Frame.GetController<DeleteObjectsViewController>().DeleteAction.ExecuteCompleted -= DeleteAction_ExecuteCompleted;
        }
    }
}
